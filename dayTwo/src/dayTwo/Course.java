package dayTwo;

public class Course {
	
	int id;
	String name;
	String desc;
	String teacher;
	double price;
	
	public Course(int id, String name, String desc, String teacher, double price) {
		this.id = id;
		this.name = name;
		this.desc = desc;
		this.teacher = teacher;
		this.price = price;
	}
}
